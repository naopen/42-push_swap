#include "../libft.h"

// If c is a letter, return 1. Otherwise, return 0.

int	ft_isalpha(int c)
{
	if ((c <= 'z' && c >= 'a')
		|| (c <= 'Z' && c >= 'A'))
		return (1);
	return (0);
}
